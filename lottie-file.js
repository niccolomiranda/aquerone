// WINGS
const container = document.querySelector("[data-scroll-container]");
const wingLowLeft = document.querySelector("[data-wing-low-left]");
const wingLowRight = document.querySelector("[data-wing-low-right]");
const wingUpLeft = document.querySelector("[data-wing-up-left]");
const wingUpRight = document.querySelector("[data-wing-up-right]");

const lerp = (x, y, a) => x * (1 - a) + y * a;
const clamp = (a, min = 0, max = 1) => Math.min(max, Math.max(min, a));

const STOP_ANGLE = -60;

const rotateWing = ({ el, stopAngle, scrollValue, speed, delay }) => {
  let angle = lerp(-scrollValue * speed + delay, stopAngle, 0.01);
  angle = clamp(angle, stopAngle, 0);

  const transform = `rotate(${angle}deg)`;
  const style = el.style;
  style["transform"] = transform;
  style["webkitTransform"] = transform;
  style["mozTransform"] = transform;
  style["msTransform"] = transform;
};

// SCROLL TEXT
const lottieWrapper = document.querySelector(".lottie__wrapper");
const speed = 5;

let animation = null;

const options = {
  renderer: "svg",
  loop: true,
  autoplay: false,
  animationData: jsonSource
};

const initAnimation = () => {
  animation = Lottie.loadAnimation({
    container: lottieWrapper,
    ...options
  });

  animation.setSubframe(false);
};

initAnimation();

// LOCOMOTIVE
const ls = new LocomotiveScroll({
  el: container,
  smooth: true
});

ls.on("scroll", (e) => {
  // WINGS
  rotateWing({
    el: wingUpLeft,
    stopAngle: STOP_ANGLE,
    scrollValue: e.scroll.y,
    speed: 0.1,
    delay: 2
  });
  rotateWing({
    el: wingUpRight,
    stopAngle: STOP_ANGLE,
    scrollValue: e.scroll.y,
    speed: 0.1,
    delay: 2
  });
  rotateWing({
    el: wingLowLeft,
    stopAngle: STOP_ANGLE,
    scrollValue: e.scroll.y,
    speed: 0.02,
    delay: 1
  });
  rotateWing({
    el: wingLowRight,
    stopAngle: STOP_ANGLE,
    scrollValue: e.scroll.y,
    speed: 0.02,
    delay: 1
  });

  // SCROLL TEXT
  animation && animation.goToAndStop(e.scroll.y * speed);
});

